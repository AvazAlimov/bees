<?php

namespace App\Http\Controllers\Admin;

use App\Equipment;
use Illuminate\Http\Request;
use App\Http\Controllers\Controller;

class AdminEquipmentController extends Controller
{
    public function __construct()
    {
        $this->middleware('auth:admin');
    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('equipment.equipment-create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required|unique:equipments'
        ]);
        $activity = new Equipment($request->all());
        $activity->save();
        return redirect()->route('admin.index')->with('message','Equipment created successfully');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        return view('equipment.equipment-update')->withFamily(Equipment::findOrFail($id));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'name'=>'required|unique:equipments,name,'.$id
        ]);
        $activity = Equipment::findOrFail($id);
        $activity->update($request->all());
        return redirect()->route('admin.index')->with('message','Equipment updated successfully');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $activity =Equipment::findOrFail($id);
        $activity->delete();
        return redirect()->back()->with('message','Equipment deleted successfully');
    }
}
